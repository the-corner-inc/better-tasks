import {createFileRoute} from '@tanstack/react-router'
import {db} from "@/db";
import {createServerFn} from "@tanstack/react-start";

// Equivalent of Server Actions (NextJS). Main difference, now in Tanstack it works not only to POST data, but to GET data too.
// It will make a server action so the client can GET / POST the last datas.
// When it need to get the data, it makes a fetch request using GET method, and runs the code (db query) from the handler into the server.
// So it tells to the Browser :
//  - A) We are on the client -> Make a fetch request
//  - B) we are on the server -> Call the code (db query), with no changes at all.
const serverLoader = createServerFn({method: 'GET' }).handler(() => {
  return db.query.todos.findMany()
})

// This constant contains createFileRoute, is HOW whe have all this typeSafety around routes in our app. It's autogenerated on a "index.tsx" page with the correct route.
export const Route = createFileRoute('/')({
  // The parameters can be many. Here component says what do we render at this route, App is rendered in "/" in this case.
  component: App,
  // Loader loads data on server & client. It tells WHAT data to load on the page.
  loader:  () => {
    console.log("loading data")
    return serverLoader()
  }
})

function App() {
  // Hook that gets the data from the loader
  const data = Route.useLoaderData()

  return <h1>Hi. You have {data.length} todos awaiting</h1>

}
