import {createFileRoute, Link, useRouter} from '@tanstack/react-router'
import {db} from "@/lib/db/db.ts";
import {createServerFn, useServerFn} from "@tanstack/react-start";
import {Badge} from "src/components/ui/badge.tsx"
import {Button} from "@/components/ui/button.tsx";
import {LogOutIcon, PlusIcon} from "lucide-react";
import {LocalCountButton} from "@/routes/(app)/todos/-feature/components/local-count-button.tsx";
import { authQueryOptions } from "@/lib/auth/auth.queries";
import {useQueryClient, useSuspenseQuery} from "@tanstack/react-query";
import {Tabs, TabsContent, TabsList, TabsTrigger} from "@/components/ui/tabs";
import {Card, CardContent, CardFooter, CardHeader, CardTitle} from "@/components/ui/card.tsx";
import { SignInTab } from "@/lib/auth/components/sign-in-tab";
import {Separator} from "@radix-ui/react-separator";
import {SocialAuthButtons} from "@/lib/auth/components/social-auth-buttons.tsx";
import {SignUpTab} from "@/lib/auth/components/sign-up-tab.tsx";
import {TodoTable} from "@/routes/(app)/todos/-feature/components/todo-table.tsx";
import authClient from "@/lib/auth/auth-client.ts";

/**
 * Home Page
 *
 * Shows different content based on authentication state:
 * - Not logged in: Login form with Sign In / Sign Up tabs
 * - Logged in: Dashboard with todos stats and quick actions
 */

// ===================================================================
// ROUTE
// ===================================================================
// This constant contains createFileRoute, is HOW whe have all this typeSafety around routes in our app. It's autogenerated on a "index.tsx" page with the correct route.
export const Route = createFileRoute('/')({
  // The parameters can be many. Here component says what do we render at this route, App is rendered in "/" in this case.
  component: HomePage,
  // Loader loads data on server & client. It tells WHAT data to load on the page.
  loader:  async ({context}) => {
      // Prefetch auth data
      const user = await context.queryClient.ensureQueryData(authQueryOptions())

      // If user is logged in, also fetch todos stats
      let stats =  user
          ? await serverLoader()
          : { total: 0, completed: 0 }


      return stats
  }
})


// ===================================================================
// HOME PAGE "/"
// ===================================================================
function HomePage() {
    // Hook that gets the data from the loader
    const data = Route.useLoaderData()

    // Get user from React Query cache
    const { data: user } = useSuspenseQuery(authQueryOptions())

    // If not logged in, show login form
    if (!user) {
        return <LoginSection />
    }

    // If logged in, show dashboard
    return <LoggedInSection />


}


// ===================================================================
// LOGIN SECTION (not authenticated)
// ===================================================================
function LoginSection() {
    return (
        <Tabs defaultValue="signin" className="max-auto w-full my-6 px-4">
            <TabsList>
                <TabsTrigger value="signin">Sign In</TabsTrigger>
                <TabsTrigger value="signup">Sign Up</TabsTrigger>
            </TabsList>

            {/* ======================================================
                TAB: Sign In
                ====================================================== */}
            <TabsContent value="signin">
                <Card>
                    <CardHeader className="text-2xl font-bold">
                        <CardTitle>Sign In</CardTitle>
                    </CardHeader>
                    <CardContent>
                        <SignInTab/>
                    </CardContent>

                    <Separator />

                    <CardFooter className="grid grid-cols-2 gap-3">
                        <SocialAuthButtons />
                    </CardFooter>
                </Card>
            </TabsContent>

            {/* ======================================================
                TAB: Sign Up
                ====================================================== */}
            <TabsContent value="signup">
                <Card>
                    <CardHeader className="text-2xl font-bold">
                        <CardTitle>Sign Up</CardTitle>
                    </CardHeader>
                    <CardContent>
                        <SignUpTab/>
                    </CardContent>
                </Card>
            </TabsContent>

        </Tabs>
    )
}


// ===================================================================
// LOGGED IN SECTION (authenticated)
// ===================================================================
async function LoggedInSection() {

    const {data: user} = useSuspenseQuery(authQueryOptions())
    const {stats} = Route.useLoaderData()

    const todosData = await serverLoader()


    const router = useRouter()
    const queryClient = useQueryClient()

    // Sign out handler
    const handleSignOut = async () => {
        await authClient.signOut()
        queryClient.setQueryData(authQueryOptions().queryKey, null)
        router.invalidate()
    }

    const pending = stats.total - stats.completed
    const completedCount = stats.completed
    const totalCount = stats.total



    return (
        <div className="min-h-screen container space-y-8">
            <div className="flex justify-between items-center gap-4">
                {/* HEADER SECTION */}
                {/* Title of page and count of todos done */}
                <div className="flex items-center justify-between">
                    <div>
                        <h1 className="text-3xl font-bold">
                            ✅ Logged in as {user?.name || user?.email}
                        </h1>
                        <p className="text-muted-foreground">
                            Auth is working! Here are your todo stats:
                        </p>
                    </div>

                    <h1 className="text-4xl font-bold">
                        Todo List
                    </h1>
                    {
                        totalCount > 0 && (
                            <Badge variant="outline">
                                {completedCount} of {totalCount} completed. {pending} Todos remaining.
                            </Badge>
                        )
                    }

                    <Button variant="outline" onClick={handleSignOut}>
                        <LogOutIcon className="mr-2 h-4 w-4" />
                        Sign Out
                    </Button>
                </div>

                {/* Buttons section  to add a todo */}
                <div className="flex gap-2">
                    <LocalCountButton/>
                    <Button size="sm" asChild>
                        <Link to={"/todos/new"}>
                            <PlusIcon /> Add Todo
                        </Link>
                    </Button>
                </div>
            </div>

            {/* BODY SECTION */}
            {/* Table of todos */}
            <TodoTable todos={todosData}/>
        </div>
    )
}


// ===================================================================
// SERVER FUNCTION
// ===================================================================
// Equivalent of Server Actions (NextJS). Main difference, now in Tanstack it works not only to POST data, but to GET data too.
// It will make a server action so the client can GET / POST the last datas.
// When it need to get the data, it makes a fetch request using GET method, and runs the code (db query) from the handler into the server.
// So it tells to the Browser :
//  - A) We are on the client -> Make a fetch request
//  - B) we are on the server -> Call the code (db query), with no changes at all.
const serverLoader = createServerFn({method: 'GET' }).handler(() => {
    return db.query.todosTable.findMany()
})

