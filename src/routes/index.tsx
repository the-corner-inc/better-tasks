// This constant contains createFileRoute, is HOW whe have all this typeSafety around routes in our app. It's autogenerated on a "index.tsx" page with the correct route.
export const Route = createFileRoute('/')({
  // The parameters can be many. Here component says what do we render at this route, App is rendered in "/" in this case.
  component: App,
  // Loader loads data on server & client. It tells WHAT data to load on the page.
  loader:  () => {
    console.log("loading data")
    return serverLoader()
  }
})

function App() {
  // Hook that gets the data from the loader
  const data = Route.useLoaderData()

  const completedCount = data.filter( todo => todo.isCompleted).length
  const totalCount = data.length


  return (
      <div className="min-h-screen container space-y-8">
        <div className="flex justify-between items-center gap-4">
          {/* HEADER SECTION */}
          {/* Title of page and count of todos done */}
          <div className="space-y-2">
            <h1 className="text-4xl font-bold">
              Todo List
            </h1>
            {
              totalCount > 0 && (
                  <Badge variant="outline">
                    {completedCount} of {totalCount} completed
                  </Badge>
                )
            }
          </div>

          {/* Buttons section  to add a todo */}
          <div className="flex gap-2">
            <LocalCountButton/>
            <Button size="sm" asChild>
              <Link to={"/todos/new"}>
                <PlusIcon /> Add Todo
              </Link>
            </Button>
          </div>
        </div>

        {/* BODY SECTION */}
        {/* Table of todos */}
        <TodoListTable todos={data}/>
      </div>

  )
}

function TodoListTable({todos,} :
  {
    todos: Array<{
      id: string
      title: string
      isCompleted: boolean
      createdAt: Date
    }>
  })
{
  // If there is no Todos
  if(todos.length === 0){
    return (
        <Empty className="border border-dashed">
          <EmptyHeader>
            <EmptyMedia variant="icon">
              <ListTodoIcon/>

            </EmptyMedia>
            <EmptyTitle>No Todos</EmptyTitle>
            <EmptyDescription>
              Try Adding a new todo
            </EmptyDescription>
          </EmptyHeader>
          <EmptyContent>
            <Button asChild>
              <Link to={"/todos/new"}>
                <PlusIcon /> Add Todo
              </Link>
            </Button>
          </EmptyContent>
        </Empty>
    )
  }

  // show all Todos
  return (
      <Table>
        <TableHeader>
          <TableRow className="hover:bg-transparent">
              <TableHead className="w-0"></TableHead>
              <TableHead>Task</TableHead>
            <TableHead>Created On</TableHead>
          </TableRow>
        </TableHeader>
        <TableBody>
          {
            todos.map(todo => (
                <TodoTableRow key={todo.id} {...todo} />
            ))
          }
        </TableBody>
      </Table>
  )

}

// Server Function
const deleteFn = createServerFn({method: "POST"})
    .inputValidator(z.object({id: z.string().min(1)} ) )
    // Server Handler
    .handler( async ({data}) => {
        await db.delete(todosTable).where(eq(todosTable.id, data.id))

        // Action button needs a promise as return
        return  {error: false}
    } )

const toggleFn = createServerFn({method: "POST"})
        .inputValidator(
            z.object({
                id: z.string().min(1),
                isCompleted: z.boolean(),
            } ) ,
        )
        // Server Handler
        .handler( async ({data}) => {
            // Timeout of 500ms - Wait before showing on screen
            //await new Promise(resolve => setTimeout(resolve, 500))
            await db.update(todosTable)
                .set({ isCompleted: data.isCompleted})
                .where(eq(todosTable.id, data.id))
        } )



function TodoTableRow({id, title, isCompleted, createdAt} : {
  id: string
  title: string
  isCompleted: boolean
  createdAt: Date
}) {

  // Wrap the methods with server Function
  const deleteFnServer = useServerFn(deleteFn)
  const toggleFnServer = useServerFn(toggleFn)

  // Use this to do a optimistic   update
  const [isCurrentCompleted, setIsCurrentCompleted] = useState<boolean>(isCompleted)

  const router = useRouter()

  return (
      <TableRow onClick={(event) => {
          const target = event.target as HTMLElement
          // If we click on actions in the row, like "edit" or "delete", we don't toggle the checkbox
          if (target.closest("[data-actios]"))
              return

          setIsCurrentCompleted(current => !current)
          // Transition to make sure it delays the update of our state for this ones
          startTransition(async () => {
              await toggleFnServer({ data: {id: id , isCompleted: !isCurrentCompleted} })
              router.invalidate()
          })

      }}>

        <TableCell>
          <Checkbox checked={isCurrentCompleted}/>
        </TableCell>

        <TableCell className={cn(
            "font-medium",
            isCurrentCompleted && "text-muted-foreground line-through")}>
          {title}
        </TableCell>

        <TableCell className="text-sm text-muted-foreground">
          {formatDate(createdAt)}
        </TableCell>

        <TableCell data-actions>
            <div className="flex items-center justify-end gap-1">
                <Button variant="ghost" size="icon-sm" asChild>
                    <Link to="/todos/$id/edit" params={{id}} >
                        <EditIcon />
                    </Link>
                </Button>

                <ActionButton
                    action={async () => {
                        const response = await deleteFnServer({data: { id } })
                        router.invalidate()
                        return response
                    }}
                    variant="destructiveGhost"
                    size="icon-sm">
                    <TrashIcon />
                </ActionButton>
            </div>
        </TableCell>

      </TableRow>
  )
}


function formatDate(date: Date) {
  const formatter = new Intl.DateTimeFormat(undefined, {
    dateStyle: "short",
  })

  return formatter.format(date)
}